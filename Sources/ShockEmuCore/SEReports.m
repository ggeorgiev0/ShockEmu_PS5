#import "ShockEmuCore.h"

#include <string.h>

SEReportStatus SECopyFeatureReport(uint8_t reportID, uint8_t *buffer, size_t *length) {
    static const uint8_t report12[] = {
        0x12, 0x8b, 0x09, 0x07, 0x6d, 0x66, 0x1c, 0x08,
        0x25, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    };
    static const uint8_t reportA3[] = {
        0xa3, 0x41, 0x75, 0x67, 0x20, 0x20, 0x33, 0x20, 0x32, 0x30,
        0x31, 0x33, 0x00, 0x00, 0x00, 0x00, 0x00, 0x30, 0x37, 0x3a,
        0x30, 0x31, 0x3a, 0x31, 0x32, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x31, 0x03, 0x00, 0x00,
        0x00, 0x49, 0x00, 0x05, 0x00, 0x00, 0x80, 0x03, 0x00,
    };
    static const uint8_t report02[] = {
        0x02, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x87, 0x22, 0x7b,
        0xdd, 0xb2, 0x22, 0x47, 0xdd, 0xbd, 0x22, 0x43, 0xdd, 0x1c,
        0x02, 0x1c, 0x02, 0x7f, 0x1e, 0x2e, 0xdf, 0x60, 0x1f, 0x4c,
        0xe0, 0x3a, 0x1d, 0xc6, 0xde, 0x08, 0x00,
    };
    const uint8_t *source = NULL;
    size_t required = 0;
    if (reportID == 0x12) {
        source = report12;
        required = sizeof(report12);
    } else if (reportID == 0xa3) {
        source = reportA3;
        required = sizeof(reportA3);
    } else if (reportID == 0x02) {
        source = report02;
        required = sizeof(report02);
    } else {
        return SEReportStatusUnsupported;
    }
    if (length == NULL) return SEReportStatusBufferTooSmall;
    size_t capacity = *length;
    *length = required;
    if (buffer == NULL || capacity < required) return SEReportStatusBufferTooSmall;
    memcpy(buffer, source, required);
    return SEReportStatusSuccess;
}
